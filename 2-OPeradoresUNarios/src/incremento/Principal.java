package incremento;

public class Principal {
    public static void main(String[] args) {
        //temos duas formas de incrementar um número

        //1 - forma.
        int n1 = 5;
        n1 = n1 + 1;//aqui ele incrementa um número, ou seja vai ficar 6
        System.out.println(n1); // SAIDA : 6

        // 2 - forma.
        int numero = 10;
        numero++; //ele incrementa o número
        System.out.println(numero);// aqui ele exibe o valor ja atualizado.


        //antecipando o incremento








    }
}
