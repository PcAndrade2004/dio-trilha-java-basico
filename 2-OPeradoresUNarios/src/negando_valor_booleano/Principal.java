package negando_valor_booleano;

public class Principal {
    public static void main(String[] args) {
        //valores booleanos tem duas alternativas (TRUE) OU (FALSE)
        //mas podemos negar esses valores.

        boolean variavel = true;//aqui incializamos a variável como (TRUE).
        System.out.println(!variavel);//estamos negando, utilizando o ( ! ) que quando colocado antes da nossa variável,
//         ele tem o papel de negar a nossa condição.

        System.out.println("\nInvertendo o valor de uma variável booleana.");
        variavel = !variavel;
        System.out.println(variavel);









    }
}
